package com.tlcsdm.game.daliandagunzifx.model;

public enum Rank {

    TWO(2, "2"),
    THREE(3, "3"),
    FOUR(4, "4"),
    FIVE(5, "5"),
    SIX(6, "6"),
    SEVEN(7, "7"),
    EIGHT(8, "8"),
    NINE(9, "9"),
    TEN(10, "10"),
    JACK(11, "J"),
    QUEEN(12, "Q"),
    KING(13, "K"),
    ACE(14, "A"),
    SMALL_JOKER(15, "小王"),
    BIG_JOKER(16, "大王");

    private final int value;
    private final String displayName;

    Rank(int value, String displayName) {
        this.value = value;
        this.displayName = displayName;
    }

    public int getValue() {
        return value;
    }

    public String getDisplayName() {
        return displayName;
    }

    public int getPoints() {
        return switch (this) {
            case FIVE -> 5;
            case TEN, KING -> 10;
            default -> 0;
        };
    }
}
